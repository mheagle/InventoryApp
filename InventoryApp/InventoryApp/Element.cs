using System;
using System.Collections.Generic;
using System.Text;



namespace InventoryApp
{
    /// <summary>
    /// This is a raw material that can be sold as an individual piece
    /// or combined with other materials to create new pieces. Unique inventory numbers are generated, and wholesale and retail amounts are calculated, 
    /// and the user can indicate when the material is sold.
    /// </summary>
    class Element
    {
        #region Properties
        /// <summary>
        /// Raw material type of the element
        /// </summary>
        public string Material { get; set; }

        /// <summary>
        /// Weight of the element in grams
        /// </summary>
        public int Weight { get; set; }

        /// <summary>
        /// Wholesale price of item
        /// </summary>
        public decimal WSPrice { get; set; }

        /// Autogenerated inventory number for the raw materials
        /// </summary>
        public string InventoryNumber { get; set; }

        /// <summary>
        /// Date acquired into inventory
        /// </summary>
        public DateTime DateAcquired { get; private set; }

        /// <summary>
        /// Location of the inventory item, warehouse, retail shop, online
        /// </summary>
        public string Location { get; set; }

        /// <summary>
        /// Wholesale cost per 1 unit of weight
        /// </summary>
        public decimal WSWeightValue { get; private set; }

        #endregion
    }



    #region Methods
    /// <summary>
    /// Determine Wholesale weight value of material as wholesale price divided by weight
    /// </summary>
    /// <param name="wvalue">wholesale value of 1 unit of weight</param>
    /// <returns></returns>
    public decimal WSWeightValue(decimal wvalue)
    {
        WSWeightValue = WSPrice / Weight;
    }
    #endregion
}